public class HerancaEComposicao {

    /*Herança e composição são duas formas de reutilizar código em Java, mas
    cada uma tem suas próprias características e propósitos.

    A herança em Java é um mecanismo pelo qual uma classe pode herdar atributos
    e métodos de outra classe. Uma classe derivada (também conhecida como
    subclasse) herda os campos e métodos da classe base (também conhecida como
     superclasse) e pode adicionar seus próprios campos e métodos. Isso
     significa que uma classe pode ser definida em termos de outra classe,
     aproveitando a funcionalidade já implementada na classe base. A herança
     é geralmente usada para modelar relações "é-um", como "um carro é um
     veículo". A herança é realizada no Java usando a palavra-chave "extends".

    Por outro lado, a composição em Java é um mecanismo pelo qual um objeto
    pode ser construído a partir de outros objetos. Em outras palavras, um
    objeto pode ter referências para outros objetos que são usados para compor
    o seu comportamento. A composição é geralmente usada para modelar relações
    "tem-um", como "um carro tem um motor". Em vez de herdar a funcionalidade
     da classe base, a classe composta usa objetos de outras classes para
     implementar sua funcionalidade. A composição é realizada no Java usando
     referências a objetos como campos de classe.

    Em resumo, a principal diferença entre herança e composição é que a herança define uma relação de
     hierarquia entre classes, enquanto a composição define uma relação de dependência entre objetos.
     A herança é útil quando queremos definir uma nova classe que é uma variação de uma classe existente,
     enquanto a composição é útil quando queremos criar objetos complexos a partir de objetos mais simples
     e reutilizáveis.*/

}
